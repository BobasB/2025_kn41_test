"""
Приклад використання assert для перевірки введення даних з клавіатури
"""

# Приклад 1: Базова перевірка assert
number = -1
try:
    assert number > 0, "число має бути більшим за нуль!"
except AssertionError as e:
    print(f"Помилка: {e}")

# Приклад 2: Перевірка введення з клавіатури
if __name__ == "__main__":
    print("=== Приклад використання assert для перевірки введення ===")
    
    # Перевірка що введено число
    a = input("Введіть число: ")
    assert a.isdigit(), "Потрібно ввести число!"
    print(f"введене число: {a}")
    
    # Додаткова перевірка: число має бути в діапазоні
    value = int(a)
    assert 1 <= value <= 100, "Число має бути в діапазоні від 1 до 100!"
    print(f"Число {value} пройшло перевірку!")
